/// A struct for representing and creating color.
#[derive(Debug)]
pub struct Color {
    /// Red channel 0 - 255
    pub r: u8,
    
    /// Green channel 0 - 255
    pub g: u8,

    /// Blue channel 0 - 255
    pub b: u8,

    /// Alpha channel 0 - 255
    pub a: u8,
}

